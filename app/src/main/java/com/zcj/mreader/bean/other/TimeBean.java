package com.zcj.mreader.bean.other;


public class TimeBean {
    private int year;
    private int month;
    private int day;
    private int hour;
    private int min;
    private int second;
    private int minSecond;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public int getMinSecond() {
        return minSecond;
    }

    public void setMinSecond(int minSecond) {
        this.minSecond = minSecond;
    }
}
